package model.bean;

public class User {
	String Username;
	String Password;
	String Ten;
	
	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getTen() {
		return Ten;
	}
	public void setTen(String hoTen) {
		Ten = hoTen;
	}
	
}
